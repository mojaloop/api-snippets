'use strict'

import fs from 'fs'
// import path from 'path'
import $RefParser from '@apidevtools/json-schema-ref-parser'

const myArgs = process.argv.slice(2)
if (myArgs.length !== 2) {
  console.log('Usage: json-scheme-generator.js <openapi-spec-file> <output-folder>')
  process.exit(1)
}

const openApiFile = myArgs[0]
const outputFolder = myArgs[1]

// console.log(file)
$RefParser.dereference(openApiFile, (err, openApiSpecObj) => {
  if (err) {
    console.error(err)
    process.exit(1)
  }
  const openApiSpec: any = openApiSpecObj
  if (openApiSpec?.components?.schemas) {
    // Write json file for schemas
    fs.writeFile(
      outputFolder + '/json-schemas.json',
      JSON.stringify(openApiSpec?.components?.schemas, null, 2),
      (err1) => {
        if (err1) {
          console.error(err1)
        }
      }
    )
    // Construct schemas file content
    let indexFileContent = '/* eslint-disable @typescript-eslint/no-namespace */'
    indexFileContent += '\n'
    indexFileContent += '\n' + '/** This file is generated by json-schema-generator.js program **/\n'
    indexFileContent += '\n' + "import JsonSchemas from './json-schemas.json'"
    indexFileContent += '\n'
    indexFileContent += '\n' + 'export namespace Schemas {'
    Object.keys(openApiSpec?.components?.schemas).forEach((schemaName) => {
      indexFileContent += '\n' + `  export const ${schemaName} = JsonSchemas.${schemaName}`
    })
    indexFileContent += '\n' + '}'
    indexFileContent += '\n'
    // Write schemas file
    fs.writeFile(outputFolder + '/schemas.ts', indexFileContent, (err1) => {
      if (err1) {
        console.error(err1)
      }
    })

    // Construct types file content
    let typesFileContent = '/* eslint-disable @typescript-eslint/no-namespace */'
    typesFileContent += '\n'
    typesFileContent += '\n' + '/** This file is generated by json-schema-generator.js program **/\n'
    typesFileContent += '\n' + "import { components } from './openapi'"
    typesFileContent += '\n'
    typesFileContent += '\n' + 'export namespace Types {'
    Object.keys(openApiSpec?.components?.schemas).forEach((schemaName) => {
      typesFileContent += '\n' + `  export type ${schemaName} = components['schemas']['${schemaName}']`
    })
    typesFileContent += '\n' + '}'
    typesFileContent += '\n'
    // Write types file
    fs.writeFile(outputFolder + '/types.ts', typesFileContent, (err1) => {
      if (err1) {
        console.error(err1)
      }
    })
  } else {
    console.log('Can not find .components.schemas in the openAPI file')
    process.exit(1)
  }
})
